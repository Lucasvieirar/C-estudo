#include <stdio.h>
/* 
    Operadores Matemáticos:
    Soma +
    Subtração -
    Multiplicação *
    Divisão /

*/
int main() {
    printf("\n Soma: %d\n", 10 + 20);
    printf("\n Subtracao: %d\n", 10 - 20);
    printf("\n Multiplicação: %d\n", 10 * 20);
    printf("\n Divisao: %.2f\n",  10.0 / 20);
   
   return 0;
}